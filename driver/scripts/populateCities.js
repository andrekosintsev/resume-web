const departureCityDropdown = document.getElementById("dCity");
const arrivalCityDropdown = document.getElementById("aCity");

// Get references to the country dropdowns
const departureCountryDropdown = document.getElementById("dCountry");
const arrivalCountryDropdown = document.getElementById("aCountry");

// Function to populate city dropdown based on selected country
function populateCityDropdown(cityDropdown, countryDropdown) {
    const selectedCountry = countryDropdown.value;

    // Clear previous options
    cityDropdown.innerHTML = "";
    let cities = [];

    switch (selectedCountry) {
        case "Беларусь":
            cities = byCities;
            break;
        case "Польша":
            cities = plCities;
            break;
        case "Литва":
            cities = ltCities;
            break;
        case "Россия":
            cities = ruCities;
            break;
        default:
            cities = [];

    }
    for (const city in cities) {
        const option = document.createElement("option");
        option.value = city;
        option.text = city;
        cityDropdown.appendChild(option);
    }
}

// Add event listeners to country dropdowns to populate city dropdowns
departureCountryDropdown.addEventListener("change", function() {
    populateCityDropdown(departureCityDropdown, departureCountryDropdown);
});

arrivalCountryDropdown.addEventListener("change", function() {
    populateCityDropdown(arrivalCityDropdown, arrivalCountryDropdown);
});

// Initialize city dropdowns based on the initial selected countries
populateCityDropdown(departureCityDropdown, departureCountryDropdown);
populateCityDropdown(arrivalCityDropdown, arrivalCountryDropdown);